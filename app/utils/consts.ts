/**
 * total milliseconds in one second
 */
export const ONE_SECOND_MS = 1000;

/**
 * total milliseconds in one minute
 */
export const ONE_MINUTE_MS = 60 * ONE_SECOND_MS;
/**
 * total milliseconds in one hour
 */
export const ONE_HOUR_MS = 60 * ONE_MINUTE_MS;
/**
 * total milliseconds in one day
 */
export const ONE_DAY_MS = 24 * ONE_HOUR_MS;
/**
 * total milliseconds in one week
 */
export const ONE_WEEK_MS = 7 * ONE_DAY_MS;
/**
 * total milliseconds in one year
 */
export const ONE_YEAR_MS = 365 * ONE_DAY_MS;
/**
 * total milliseconds in 5 seconds
 */
export const FIVE_SECONDS_MS = 5 * ONE_SECOND_MS;
/**
 * total milliseconds in 5 minutes
 */
export const FIVE_MINUTES_MS = 5 * ONE_MINUTE_MS;
